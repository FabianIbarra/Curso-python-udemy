import os
os.system("cls" if os.name == "nt" else "clear")

# Suma
a = 5 + 3
print(f"Suma: 5 + 3 = {a}")

# Resta
b = 5 - 3
print(f"Resta: 5 - 3 = {b}")

# Multiplicación
c = 5 * 3
print(f"Multiplicación: 5 * 3 = {c}")

# División
d = 5 / 3
print(f"División: 5 / 3 = {d}")

# División sin decimales (entera)
e = 5 // 3
print(f"División entera: 5 // 3 = {e}")

# Módulo
f = 5 % 3
print(f"Módulo: 5 % 3 = {f}")

# Potencia
g = 5 ** 3
print(f"Potencia: 5 ** 3 = {g}")

# Raíz cuadrada
import math
h = math.sqrt(25)
print(f"Raíz cuadrada de 25: {h}")

# Redondear
i = round(5.6789, 2)
print(f"Redondear 5.6789 a 2 decimales: {i}")

# Valor absoluto
j = abs(-10)
print(f"Valor absoluto de -10: {j}")